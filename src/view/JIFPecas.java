private boolean preencherObjeto(){
    obj.setId(Integer.parseInt(jlIdprod.getText()));
    obj.setDescricao(jtfdescricao.getText());
    obj.setCusto(Float.parseFloat(jtfprecocusto.getText()));
    obj.setPreco_venda(Float.parseFloat(jtfprecovenda.getText()));
    ;
    
    return true;
}

private boolean validarCampos(){
    String msg = "";
    if(jtfdescricao.getText().length()<5)
        msg+="\nA descrição deve ter pelo menos 5 caracteres!";
    if(jtfdescricao.getText().length()>100)
        msg+="\nA descrição deve ter menos de 100 caracteres!"; 
    if(jtfprecocusto.getText().length()==0)
        msg+="\nDigite o preço de custo!";
    if(jtfprecovenda.getText().length()==0)
        msg+="\nDigite o valor de venda!";
    if(msg.length()==0)
       return true;
    else{
       JOptionPane.showMessageDialog(this, "Corrija os seguintes campos" + msg, "Meu sistema", JOptionPane.ERROR_MESSAGE);
       return false;
    }
}
