    private void jtfPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisarKeyReleased
        try {DefaultTableModel mp = (DefaultTableModel) jtbpecas.getModel();
            mp.setNumRows(0);
            if(jtfPesquisar.getText().length()>=0){
                listadePecas=DAO.Pesquisar(jtfPesquisar.getText());
                for(Pecas p: listadePecas){
                    mp.addRow(new String[]{""+p.getId(),p.getDescricao(),""+p.getCusto(),""+p.getPreco_venda()});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:"+e.getMessage(),"Erro ao pesquisar",JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jtfPesquisarKeyReleased

    private void jtbpecasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbpecasMouseClicked
         try {
            int linhaSelecionada = jtbpecas.getSelectedRow();
            if(linhaSelecionada>-1){
                jlIdprod.setText(""+listadePecas.get(linhaSelecionada).getId());
                jtfdescricao.setText(listadePecas.get(linhaSelecionada).getDescricao());
                jtfprecocusto.setText(""+listadePecas.get(linhaSelecionada).getCusto());
                jtfprecovenda.setText(""+listadePecas.get(linhaSelecionada).getPreco_venda());
                jbexcluir.setEnabled(true);
                jbsalvarpeca.setEnabled(false);
                jbeditar.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:"+e.getMessage(),"Erro ao pesquisar",JOptionPane.ERROR_MESSAGE);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jtbpecasMouseClicked

